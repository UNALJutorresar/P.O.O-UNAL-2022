    private void gocilindrobttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gocilindrobttnActionPerformed
        boolean error=false;
        try{
            double radio=Double.parseDouble(radioinput.getText());
            double height=Double.parseDouble(heightinput.getText());
            cilindro cil=new cilindro(radio, height);
            voltxt.setText(String.format("%.2f",cil.calcVol()));
            surftxt.setText(String.format("%.2f", cil.calcSurf()));
        } catch(NumberFormatException e){
            error=true;
        } finally {
            if(error){
                JOptionPane.showMessageDialog(rootPane, "Campo vac√≠o o formato de numero incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_gocilindrobttnActionPerformed

